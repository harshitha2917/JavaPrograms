package module1programs;
//Assignment-2
public class Datatypes_Variables {
	public static void main(String[] args) {
		//declaration and initialization of data types and variables

		byte a = 10; // -127 to 128
		System.out.println(a);

		short b = 32123; // -32768 to 32767
		System.out.println(b);

		int c = 98765;// 32 bits
		System.out.println(c);

		long d = 987654;// 64 bits
		System.out.println(d);

		float e = 5.43f;// decimal value + f
		System.out.println(e);

		double d1 = 876.432;
		System.out.println(d1);

		char c1 = 'J';// declare inside single quote
		System.out.println(c1);

		boolean b1 = true;
		System.out.println(b1);

		boolean b2 = false;
		System.out.println(b2);

		String name = "Manish Kumar Tiwari";
		System.out.println(name);

	}
}
