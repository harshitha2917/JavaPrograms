package module1programs;

public class Math_Class {
public static void main(String[] args) {
	System.out.println(Math.addExact(90, 80));
	
	System.out.println(Math.subtractExact(90, 80));
	
	System.out.println(Math.multiplyExact(90, 80));
	
	System.out.println(Math.min(90, 80));//15 Methods
	
	System.out.println(Math.max(90, 80));//11 Methods
	
	System.out.println(Math.abs(-90.98));//19 Methods gives positive value
	
	System.out.println(Math.random());
	
	double a=Math.random();// we can store Math.random in double value
	System.out.println(a);
	
	double pi=Math.PI;//gives exact pi value
	System.out.println(pi);
}
}
